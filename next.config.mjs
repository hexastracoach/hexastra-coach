/**
 * Content Security Policy — adapté à Next.js + Supabase + Stripe + Google Fonts.
 *
 * Notes prod :
 * - 'unsafe-inline' pour scripts requis par Next.js (hydration inline)
 * - 'unsafe-eval' peut être retiré si on adopte une approche nonce (Next 15+)
 * - connect-src inclut les domaines Supabase et OpenAI (server-side uniquement via API routes)
 * - frame-src pour Stripe Checkout iframe
 *
 * TODO Vercel : activer Vercel Edge Config pour injecter un nonce CSP dynamique
 * et supprimer 'unsafe-inline' + 'unsafe-eval'.
 */
const ContentSecurityPolicy = [
  "default-src 'self'",
  "script-src 'self' 'unsafe-inline' 'unsafe-eval' https://js.stripe.com",
  "style-src 'self' 'unsafe-inline' https://fonts.googleapis.com",
  "font-src 'self' https://fonts.gstatic.com",
  "img-src 'self' data: blob: https:",
  "media-src 'self'",
  "connect-src 'self' https://*.supabase.co wss://*.supabase.co https://api.stripe.com",
  "frame-src https://js.stripe.com https://hooks.stripe.com",
  "object-src 'none'",
  "base-uri 'self'",
  "form-action 'self'",
  "upgrade-insecure-requests",
].join('; ')

const securityHeaders = [
  { key: 'Content-Security-Policy', value: ContentSecurityPolicy },
  { key: 'X-Content-Type-Options', value: 'nosniff' },
  { key: 'X-Frame-Options', value: 'DENY' },
  { key: 'Referrer-Policy', value: 'strict-origin-when-cross-origin' },
  { key: 'X-DNS-Prefetch-Control', value: 'off' },
  { key: 'Permissions-Policy', value: 'camera=(), microphone=(), geolocation=()' },
]

/** @type {import('next').NextConfig} */
const nextConfig = {
  poweredByHeader: false,
  reactStrictMode: true,
  async headers() {
    return [
      {
        source: '/:path*',
        headers: securityHeaders,
      },
    ]
  },
}

export default nextConfig
